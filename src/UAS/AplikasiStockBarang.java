    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        if (pilih.getSelectedIndex() == 1) {

            try {
                selectBykode(Integer.parseInt(kode.getText()));
            } catch (SQLException ex) {
                Logger.getLogger(AplikasiStockBarang.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (pilih.getSelectedIndex() == 2) {
            selectByNama(nama.getText());
        } else if (pilih.getSelectedIndex() == 3) {
            selectByawal(Integer.parseInt(awal.getText()));
        } else if (pilih.getSelectedIndex() == 4) {
            selectBysatuan(satuan.getText());  
        }else if (pilih.getSelectedIndex() == 5) {
            selectByakhir(Integer.parseInt(akhir.getText()));
        } else {
            table();
        }
        
    }//GEN-LAST:event_cariActionPerformed

