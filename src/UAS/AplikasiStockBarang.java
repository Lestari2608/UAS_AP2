    private void tabelstockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelstockMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tabelstock.getModel(); //create a model
        int selectedRowIndex = tabelstock.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        KoneksiSQL konek = new KoneksiSQL();
        konek.koneksi();


        try {

            String sql = "select * from swalayan where kodebarang like '%" + id + "%'";
            pst = (Statement) konek.con.prepareStatement(sql);
            ResultSet rs = rs = pst.executeQuery(sql);

            while (rs.next()) {    //u can use normal query
                kode.setText(rs.getString("kodebarang"));
                nama.setText(rs.getString("namabarang"));
                awal.setText(rs.getString("stockawal"));
                satuan.setText(rs.getString("Satuan"));
                akhir.setText(rs.getString("stockakhir"));
            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_tabelstockMouseClicked

